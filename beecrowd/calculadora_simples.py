# Entrada.
x = int(input('Digite o primeiro valor: '))
y = int(input('Digite o segundo valor: '))

soma = x + y # Soma entre os valores
subtracao = x - y # Subtração entre os valores
multiplicacao = x * y # Multiplicação entre os valores
divisao = x / y # Divisão entre os valores

# Saída, irá mostrar os valores calculados.
print('Soma =', soma)
print('Subtração =', subtracao)
print('Multiplicação =', multiplicacao)
print('Divisão =', divisao)